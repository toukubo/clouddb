package net.enclosing.list;

public class Custom {
	private String name = "";
	private boolean kitable = false;
	private String kit = "";
	private String project = "";
	private String bufferable = "";
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public boolean isKitable() {
		return kitable;
	}
	public void setKitable(boolean kitable) {
		this.kitable = kitable;
	}
	public String getKit() {
		return kit;
	}
	public void setKit(String kit) {
		this.kit = kit;
	}
	public String getProject() {
		return project;
	}
	public void setProject(String project) {
		this.project = project;
	}
	public String getBufferable() {
		return bufferable;
	}
	public void setBufferable(String bufferable) {
		this.bufferable = bufferable;
	}
	
			
}
